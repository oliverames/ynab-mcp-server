#!/usr/bin/env node

import { execFile } from "node:child_process";
import { createHash } from "node:crypto";
import { mkdtemp, readFile, rm, writeFile } from "node:fs/promises";
import { tmpdir } from "node:os";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import { promisify } from "node:util";

const execFileAsync = promisify(execFile);
const repoRoot = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const sourcePath = join(repoRoot, "codex", "assets", "icon.png");
const outputPath = join(repoRoot, "worker", "src", "connector-icon-assets.js");
const temporaryDirectory = await mkdtemp(join(tmpdir(), "ynab-connector-icons-"));

const variants = [
  { name: "CONNECTOR_FAVICON_16_PNG", filename: "favicon-16x16.png", size: 16 },
  { name: "CONNECTOR_FAVICON_32_PNG", filename: "favicon-32x32.png", size: 32 },
  { name: "CONNECTOR_APPLE_TOUCH_ICON_PNG", filename: "apple-touch-icon.png", size: 180 },
];

function sha256(buffer) {
  return createHash("sha256").update(buffer).digest("hex");
}

function exportedAsset(name, buffer) {
  return `export const ${name}_SHA256 = "${sha256(buffer)}";\n` +
    `const ${name}_BASE64 = "${buffer.toString("base64")}";\n` +
    `export const ${name} = decodeBase64(${name}_BASE64);\n`;
}

try {
  const source = await readFile(sourcePath);
  const generated = [];

  for (const variant of variants) {
    const generatedPath = join(temporaryDirectory, variant.filename);
    await execFileAsync("magick", [
      sourcePath,
      "-strip",
      "-resize",
      `${variant.size}x${variant.size}`,
      "-define",
      "png:exclude-chunks=date,time",
      generatedPath,
    ]);
    generated.push({ name: variant.name, buffer: await readFile(generatedPath) });
  }

  const faviconPath = join(temporaryDirectory, "favicon.ico");
  await execFileAsync("magick", [
    sourcePath,
    "-strip",
    "-define",
    "icon:auto-resize=16,32,48",
    faviconPath,
  ]);
  generated.push({ name: "CONNECTOR_FAVICON_ICO", buffer: await readFile(faviconPath) });

  const output = [
    "// Generated by scripts/generate-worker-icon-assets.mjs.",
    "// Canonical source: codex/assets/icon.png (the icon displayed by Codex).",
    "// Run `npm run build:worker-icons` after changing the canonical icon.",
    "",
    "function decodeBase64(value) {",
    "  return Uint8Array.from(atob(value), (character) => character.charCodeAt(0));",
    "}",
    "",
    exportedAsset("CONNECTOR_ICON_PNG", source),
    ...generated.map(({ name, buffer }) => exportedAsset(name, buffer)),
  ].join("\n");

  await writeFile(outputPath, output);
  console.log(`Generated ${outputPath} from ${sourcePath}`);
} finally {
  await rm(temporaryDirectory, { recursive: true, force: true });
}
